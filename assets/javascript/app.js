// function that makes the search buttons
function makeButton(text) {
  // Check if input is a pokemon
  for (var i = 0; i < pokemon.length; i++) {
    if (pokemon[i].indexOf(text.toLowerCase()) != -1) {
      // If so, make a button
      var button = $("<button class='search btn " + pokemon[i][1] + "' id=" + text + " >" + text.toUpperCase() + "</button>");
      $("#buttons").append(button);
      var isPokemon = true;
    }
  }
  $("#user-value").val("");
  if (!isPokemon) {
    $("#user-value").attr("placeholder", "THAT'S NOT A POKEMON");
  }
};

// make buttons for everything in the starter array
for (var i = 0; i < buttonArr.length; i++) {
  makeButton(buttonArr[i]);  
};

